package com.ssafy.ws.step3;

/**
 * 직각삼각형 모양의 숫자 출력하는 클래스
 */
public class DigitTest1 {

	public static void main(String[] args) {
		for (int i=1; i<=5; i++) {
			System.out.print(i + "  ");
		}
		System.out.print("\n   ");
		for (int i=6; i<=9; i++) {
			System.out.print(i + "  ");
		}
		System.out.print("\n      ");
		for (int i=10; i<=12; i++) {
			System.out.print(i + " ");
		}
		System.out.print("\n         ");
		for (int i=13; i<=14; i++) {
			System.out.print(i + " ");
		}
		System.out.print("\n            ");
		for (int i=15; i<=15; i++) {
			System.out.print(i + "  ");
		}
	}
}
